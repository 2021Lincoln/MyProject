#Demonstração de expressão complexas

#Declaração de variaveis que iremos usar..

x = 10; y = 20; z = 30

VALOR = x * (y / 10 + z / 10) - 25
print(f"O resultado da expressao #1 é: {VALOR}")

VALOR = ((y - x) + (z - x)) / 3
print(f"O resultado da expressão #2 é: {VALOR}")

VALOR = (x + (y * 2 +(z / 10))) - 18
print(f"O resultado da expressão #3 é {VALOR}")